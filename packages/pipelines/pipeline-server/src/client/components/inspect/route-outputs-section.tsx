import { useInspectData } from "#hooks/use-inspect-data";
import { Button } from "@ucdjs-internal/shared-ui/ui/button";
import { ArrowRight, FolderOutput } from "lucide-react";

export function RouteOutputsSection() {
  const { selectedRoute, navigateToOutput } = useInspectData();

  if (!selectedRoute) return null;

  return (
    <section className="space-y-3">
      <div className="flex items-center gap-2">
        <FolderOutput className="h-4 w-4 text-muted-foreground" />
        <h3 className="text-xs uppercase tracking-wide text-muted-foreground">Outputs</h3>
      </div>
      {selectedRoute.outputs.length
        ? (
            <div className="grid gap-3 md:grid-cols-2">
              {selectedRoute.outputs.map((output, index) => (
                <div key={`${output.dir ?? "none"}-${output.fileName ?? "none"}-${index}`} className="rounded-lg border border-border/60 bg-muted/10 p-4">
                  <div className="flex items-center justify-between gap-3">
                    <div className="text-sm font-medium">
                      Output
                      {index + 1}
                    </div>
                    <Button
                      type="button"
                      variant="outline"
                      size="sm"
                      onClick={() => navigateToOutput(selectedRoute.id, index)}
                    >
                      Open output
                      <ArrowRight className="h-3 w-3" />
                    </Button>
                  </div>
                  <div className="mt-3 grid gap-3">
                    <div className="space-y-1">
                      <div className="text-[11px] uppercase tracking-wide text-muted-foreground">Directory</div>
                      <div className="rounded-md border border-border/60 bg-background px-3 py-2 text-sm text-foreground">
                        {output.dir ?? "Default route output directory"}
                      </div>
                    </div>
                    <div className="space-y-1">
                      <div className="text-[11px] uppercase tracking-wide text-muted-foreground">File name</div>
                      <div className="rounded-md border border-border/60 bg-background px-3 py-2 text-sm text-foreground">
                        {output.fileName ?? "Generated by route configuration"}
                      </div>
                    </div>
                  </div>
                </div>
              ))}
            </div>
          )
        : (
            <div className="rounded-md border border-dashed border-border/70 px-4 py-8 text-sm text-muted-foreground">
              No output definitions for this route.
            </div>
          )}
    </section>
  );
}
